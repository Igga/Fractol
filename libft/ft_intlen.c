unsigned int	ft_intlen(int a)
{
	unsigned int	n;
	int				ans;

	ans = 0;
	if (a < 0)
	{
		ans++;
		n = -a;
	}
	else
		n = a;
	while ((n = n / 10))
		ans++;
	return (ans);
}
